/**
 * @param {string} str1
 * @param {string} str2
 * @return {string}
 */
let gcdOfStrings = function(str1, str2) {
  let str1Array = []
  let str2Array = []

  for ( let a = 0; a<str1Array.length; a++)
  {
    str1Array.push(str1[a])

  }
  for(let b = 0;b<str2Array.length; b++){
    str2Array.push(str2[b])
  }

}
gcdOfStrings("BCD", "XYZ")